    int num1;
    int num2;

     // Método para sumar dos números enteros y devolver el resultado
    public int suma(int num1, int num2) {
        return num1 + num2;
    }

    // Método para restar dos números enteros y devolver el resultado
    public int resta(int num1, int num2) {
        return num1 - num2;
    }

    // Método para multiplicar dos números enteros y devolver el resultado
    public int multiplicacion(int num1, int num2) {
        return num1 * num2;
    }
