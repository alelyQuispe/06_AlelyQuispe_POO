    void listar() {
            String sql = "SELECT * FROM persona";
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                rs = st.executeQuery(sql);
                
                Object[]persona = new Object[3];
                modelo = (DefaultTableModel) TablaDatos.getModel();
                
                while (rs.next()) {
                    
                    persona[0] = rs.getInt("Id");
                    persona[1] = rs.getString("Nombres");
                    persona[2] = rs.getInt("telefono");
                    modelo.addRow(persona);
                }
                TablaDatos.setModel(modelo);
            } catch (Exception e) {
                System.out.println("ERROR LISTAR: " + e);
            }
    }
    
    void  agregar() {
        
        String nombre = txtNombre.getText();
        String telefono = txtTelefono.getText();
        
        String sql = "INSERT INTO persona (Nombres, telefono) VALUES ('" + nombre + "' , " + telefono + ")";
        if (nombre.equals("") || telefono.equals("")) {
            System.out.println("cajas vacias");
        } else {
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                limpiar();
            } catch (Exception e) {
            }
        }    
    }
    
    void limpiar() {
        for (int i = 0; i <= TablaDatos.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
    
    void modificar() {
    String nom = txtNombre.getText();
    String tel = txtTelefono.getText();
    String sql = "update persona set Nombres = '" + nom + "', telefono = '" + tel + "' where id = " + id;

    if (nom.equals("")) {
        System.out.println("Ingresa el nombre");
    } else {
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Usuario Actualizado");
            limpiar();
        } catch (Exception e) {
        }
    }
}
    
    void eliminar() {
    int filaseleccionado = TablaDatos.getSelectedRow();

    if (filaseleccionado == -1) {
        System.out.println("Debe seleccionar Fila");
    } else {
        String sql = "delete from persona where id = " + id;

        try {
            cn = con.getConnection();
            st = cn.createStatement();
            st.executeUpdate(sql);
            limpiar(); // Limpia la tabla despuÃ©s de eliminar el registro
            listar(); // Vuelve a cargar los datos en la tabla
        } catch (Exception e) {
        }
    }
}
    
